static void serialout(int ch)
{
        while (IOSPACE[1])
                ;
        IOSPACE[0] = ch;
}


static void putchar(char ch)
{
        unsigned char *d = fb + x + 16 * W * y;
        unsigned char *s = chars_bitmaps + 16*(unsigned char)ch;
        unsigned i;

        if (ch >= ' ') {
                for (i = 0; i < 16; ++i, d += W, ++s) {
                        *d = *s;
                        // printf("%x\n", p[i]);
                }
                ++x;
        }

        if (x == 80 || ch == '\n') {
                x = 0;
                ++y;
                if (y == 30) {
                        y = 29;
                        for (i = 0; i < 10 * (480 - 16); ++i)
                                ((long *)fb)[i] = ((long *)fb)[i + 160];
                        for (; i < 10 * 480; ++i)
                                ((long *)fb)[i] = 0;
                }
        }
}

static void puts(char *s)
{
        while (*s)
                putchar(*s++);
}

void puthex(int d, unsigned long v)
{
        if (d > 1) puthex(d - 1, v >> 4);
        putchar("0123456789abcdef"[v & 15]);
}

